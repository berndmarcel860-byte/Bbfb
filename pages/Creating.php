    function changeStatusText() {
        var statusText = document.getElementById('statusText');
        var hiddenTexts = document.getElementsByClassName('hidden-text');
        var currentIndex = 0;
        var interval = getRandomInterval(2000, 3000, 4000, 5000);

        setInterval(function() {
            statusText.style.opacity = 0;

            setTimeout(function() {
                currentIndex = (currentIndex + 1) % hiddenTexts.length;
                statusText.textContent = hiddenTexts[currentIndex].textContent;
                statusText.style.opacity = 1;
            }, 500);
        }, interval);
    }

    function getRandomInterval(min, max) {
        return Math.floor(Math.random() * (max - min + 1)) + min;
    }

    // Start the text rotation when page loads
    changeStatusText();
  </script>

  
